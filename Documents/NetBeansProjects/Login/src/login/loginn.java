    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:

    String user = username.getText();
    String pass = new String(password.getPassword());


    String hashedPassword = PasswordUtil.hashPassword(pass);

    String sql = "SELECT * FROM signupihm WHERE username = ? AND password = ?";

    try (Connection conn = DatabaseConnection.getConnection(); 
         PreparedStatement stmt = conn.prepareStatement(sql)) {

        stmt.setString(1, user);
        stmt.setString(2, hashedPassword);

        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Login successful!");
             dispose();
            Homepage hpage = new Homepage();
            hpage.setWelcomeMessage(user); 
            hpage.show();
            
            // Redirect to another page or perform further actions
        } else {
            // Login failed
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid username or password.");
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
    }
    }//GEN-LAST:event_loginBtnActionPerformed

