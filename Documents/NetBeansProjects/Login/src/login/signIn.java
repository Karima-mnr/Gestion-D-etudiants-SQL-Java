    private void signupbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupbtnActionPerformed
        // TODO add your handling code here:
 
    String usernameText = username.getText();
    String emailText = email.getText();
    String passwordText = new String(password.getPassword());   
    String confirmPasswordText = new String(confirmPassword.getPassword()); 


    if (usernameText.isEmpty() || emailText.isEmpty() || passwordText.isEmpty() || confirmPasswordText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!passwordText.equals(confirmPasswordText)) {
        JOptionPane.showMessageDialog(this, "Passwords do not match!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }


    String hashedPassword = PasswordUtil.hashPassword(passwordText);
    String hashedConfirmPassword = PasswordUtil.hashPassword(confirmPasswordText);


    try (Connection conn = DatabaseConnection.getConnection()) {
        String sql = "INSERT INTO signupihm (username, email, password, confirmpassword) VALUES (?, ?, ?, ?)";
        PreparedStatement stmt = conn.prepareStatement(sql);

        stmt.setString(1, usernameText);
        stmt.setString(2, emailText);
        stmt.setString(3, hashedPassword);
        stmt.setString(4, hashedConfirmPassword);

        int rowsInserted = stmt.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Sign-up successful!", "Success", JOptionPane.INFORMATION_MESSAGE);

        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_signupbtnActionPerformed

